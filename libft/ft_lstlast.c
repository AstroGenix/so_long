#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	while (lst->next)
		lst = lst->next;
	return (lst);
}

/* 04
- - Description
Returns the last node of the list.
- - Parameters
lst: The beginning of the list.
- - Return
The last node of the list.
*/